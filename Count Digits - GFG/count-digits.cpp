class Solution{
public:
    int evenlyDivides(int N){
        int num = N, ans = 0;
        while(N > 0){
            int r = (N % 10);
            N /= 10;
            if(r == 0){
                continue;
            }
            if(num % r == 0){
                ans++;
            }
        }
        return ans;
    }
};


class Solution {
  public:
    int equalSum(int n,vector<int> &A) {
        long long int ls = 0, rs = 0;
        rs = accumulate(A.begin(),A.end(),0);
        for(int i = 0; i < n; i++){
            rs -= A[i];
            if(ls == rs){
                return i + 1;
            }
            ls += A[i];
        }
        return -1;
    }
};



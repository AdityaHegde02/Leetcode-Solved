class Solution{
    public:
    struct Node* modifyTheList(struct Node *head)
    {
        vector<int> a;
        Node* temp = head;
        while(temp != NULL){
            a.push_back(temp->data);
            temp = temp -> next;
        }
        
        int sz = a.size();
        for(int i = 0; i < (sz/2); ++i){
            int temp = a[i];
            a[i] = (a[sz - i - 1]) - a[i];
            a[sz - i - 1] = temp;
        }
        
        int i = 0;
        temp = head;
        while(temp){
            temp -> data = a[i++];
            temp = temp -> next;
        }
        
        return head;
    }
};

